Odgojni rad u Domu ostvaruju odgajatelji. Oni su pedagoški voditelji i organizatori odgojnih aktivnosti.Odgojni rad u Domu je vrlo cjelovit. Očituje se na svim temeljnim odgojnim područjima.Zato i za njega vrijede osnovni zahtjevi u području tjelesnog, intelektualnog, moralnog, estetskog i radnog I funkcionalnog odgoja.On ima svoje specifičnosti koje u prvi plan istiću nekoliko posebno vrijednih principa u području odgoja u domu. <br><br>

Takvi principi su: aktivnost, postavljanje zahtjeva, poštovanja osobnosti odgajanika, socijalizacije, individualizacije, posrednog pristupanja odgajaniku, minimalne segregacije i jedinstva odgojnih uticaja.<br><br>

S obzirom da odgoj u domu obuhvata cijelu ličnost odgajanika, od velikog značaja su svi poznati pedagoški principi kao putokazi u radu, a neki od njih u domskom odgoju imaju posebno značenje i vrijednost, kao što su:<br><br>

Princip individualizacije <br>
Princip svjesne aktivnosti <br>
Princip postavljanja zahtjeva i poštovanja osobnosti svakog djeteta <br>
Princip socijalizacije <br>
Princip posrednog (neizravnog) pristupanja djetetu <br>
Princip jedinstva odgojnih utjecaja. <br>
Princip minimalne segregacije <br>
 Metode odgoja također će imati svakodnevnu primjenu, a načini i metode rada će se određivati u skladu sa potrebama pravilnog rasta i razvoja za dijete.<br><br>

U skladu s tim koriste se slijedeće metode:<br><br>

Metoda razgovora /savjet, razgovor, objašnjenje <br>
Metoda poučavanja<br>
Metoda uvjeravanja i vrednovanja /vrednovanje, stvaranje ideala, pozitivno upravljeno vrednovanje<br>
Metoda navikavanja /navikavanje u randnim postupcima, vježbanje u dobrim postupcima<br>
Metoda podsticanja i pomaganja<br>
Metoda sprečavanja i prisiljavanja /odvikavanje, preodgajanje/<br>
Odgojni rad u domu zahtijeva od odgajatelja primjenu određenih metoda radi upoznavanja I rada sa djetetom. Posmatranje i praćenje djeteta ima važan zadatak u daljem radu i cjelokupnom odgoju djeteta.<br><br>

Pored gore navedenih principa i metoda u odgoju koji su zastupljeni u radu sa djecom, kod djece kod kojih su primjetna odstupanja važno je posebno napraviti individualni plan rada u koji se uključuju svi raspoloži resursi u cilju pomoći djetetu, u svim sferama odgoja.




