<p>
Osnovni zadaci Doma su odgoj djece bez roditeljskog staranja u skladu sa opštim odgojnim ciljem, a specifični zadaci proizilaze iz specifičnosti uzroka zbog kojih je dijete došlo u Dom, kao i specifičnih razvojnih mogućnosti i potreba svakog pojedinog odgajanika.<br><br>

U ostvarivanju postavljenih ciljeva Dom, polazi od opštih ciljeva. Konkretizacija i realizacija ovako postavljenih ciljeva odgoja zahtijeva da se u Domu sistematski i organizovano u zajednici sa drugim  faktorima, stvaraju uslovi za njihovu realizaciju, a naročito kroz obezbjeđivanje :<br><br>

uslova za korekciju određenih poteškoća u rastu i razvoju <br>
uslova za pravilan fizički razvoj odgajanika<br>
uslova za optimalan, intelekualni i kognitivni razvoj<br>
uslova za normalan razvoj jezika, glasa i govora kod djece<br>
uslova za mogućnost individualnog i individualiziranog pristupa svakom djetetu uz poštivanje osobnosti i stvaranjem uslova za stimulanse i stimulirajuću sredinu.<br>
uslova za socijalizaciju i resocijalizaciju.<br>
uslova za zadovoljavanje osnovnih potreba djece u cilju pravilnog rasta i razvoja<br>
uslova za pravilnu i korisnu organizaciju slobodnog vremena odgajanika<br>
uslova dobre saradnje sa lokalnom zajednicom u kojima će se odgajanici formirati kao društveno-pozitivne i moralno pravilno usmjerene ličnosti<br>
uslova za estetski odgoj<br>
Djelatnost Doma je socijalno zbrinjavanje djece i omladine bez odgovarajućeg roditeljskog staranja, čiji je razvoj ometen porodičnim prilikama sve do obezbjeđenja uslova za njihov povratak u biološku porodicu, uključivanje u život izvan Doma ili dok im se ne pruži neka druga odgovarajuća zaštita.<br><br>

Djeci se obezbjedjuje siguran smještaj i briga, socijalne usluge iz djelatnosti institucije i okruženje kome su dostupne raznovrsne socijalne, zdravstvene, kulturno-zabavne, sportske i rekreativne usluge u skladu sa identifikovanim potrebama I interesima djeteta.<br><br>

Ustanova obezbjeđuje programe koji su posebno prilagođeni mogućnostima i interesovanjima svakog djeteta.<br><br>

U Dom se zbrinjavaju djeca od 0-18 godina, odnosno do završetka srednjoškolskog obrazovanja. Odluku o zbrinjavanju djeteta u Dom donosi Stručni tim Ustanove uz saglasnost Ministarstva za rad, socijalnu politiku i povratak TK.<br><br>

Osnovna djelatnost Doma je zadovoljavanje osnovnih potreba djeteta:<br><br>

zadovoljavanje materijalnih potreba (stanovanje, ishranu, odjeću, obuću);
zadovoljavanje  higijenskih i kulturnih potreba;
zadovoljenje socijalnih potreba;
zadovoljavanje zdravstvenih potreba;
odgoj i obrazovanje, odnosno, aktivan odnos odgajatelja prema djetetu s ciljem odrastanja mentalno zdravog djeteta, svestrano obrazovanog i odgojenog djeteta.




