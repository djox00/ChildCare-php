<p> 
Prva organizovana aktivnost na zbrinjavanju djece lišene roditeljskog staranja u općini Tuzla pokrenuta je početkom šezdesetih godina u okviru Centra za socijalni rad. Organizovanom društvenom brigom uz pomoć nadležnih institucija i organa društveno-političke zajednice, oktobra 1962.godine u bivši Dom učenika voćarske škole smještena su djeca lišene roditeljskog staranja sa šireg područja tuzlanske regije.<br><br>

Sadašnji dom je prvobitno započeo djelatnost kao stacionar za djecu i omladinu, a od 19.06.1967.godine rješenjem Okružnog privredog suda u Tuzli br. 04/67 godine osniva se kao samostalna Ustanova registracijom pod nazivom Dječiji dom Vojo Perić Tuzla, konstituisana je kao RO bez OOUR-a i RJ krajem 1973.godine i upisana u sudski registar pod brojem :49. kod Okružnog privrednog suda u Tuzli.<br><br>

Do 1980.godine Dom je radio pod vrlo teškim materijalnim uslovima u dosta skučenimnkapacitetima, a od 1980.godine završen je i predat na upotrebu objekta tipa dom-porodica sa savremenijim uslovima za život i rad djece.<br>

U periodu od 1992-95 godine rad i aktivnosti Doma se značajnije usložnjava pri čemu je dat prioritet pojačanoj bezbijednosti djece i izvršavanju osnovnih zadataka neophodnih za bezbjedan i siguran boravak djece u Domu. Navedene i druge teškoće na vrijeme su rješavane u granicama mogućnosti Doma i pomoći koje su pružale humanitarne organizacije i šira društvena zajednica.<br><br>

Djelatnost Doma odvijala se u skladu sa Uredbom sa zakonskom snagom o Ustanovama. Shodno odredbama ove Uredbe Skupština opštine Tuzla je donijela Odluku o preuzimanju prava osnivača prema Domu i njegovu transformaciju u Javnu ustanovu Dom za djecu bez roditeljskog staranja Tuzla.U skladu sa Odlukom izvršene su neophodne aktivnosti, formiran je Upravni odbor Doma, donesena su Pravila o organizovanju Ustanove, izvršena je registracija Doma kao Javna Ustanova Dom za djecu bez roditeljskog staranja i druge mjere i aktivnosti koje predviđa Zakon o preuzimanju prava osnivača.<br><br>

Skupština Tuzlansko-Podrinjskog kantona na sjednici od 22.05.1997.godine (sada Tuzlanski kanton) donijela je Zakon o preuzimanju prava osnivača nad JU Dom za djecu bez roditeljskog staranja, s obzirom da su se u tom periodu kao i danas uglavnom prihvatila i prihvataju djeca sa područja šire zajednice. Shodno ovom Zakonu i drugim pozitivnim propisima koji tretiraju ovu problematiku izvršena je registracija ustanove za prijem i prihvat djece od 0-18 godina starosti. S tim u vezi u okviru postojećeg objekta dograđen je aneks površine 350 kvadratnih metara za prijem i prihvat oko 40-oro djece uzrasne dobi od 0-5 godina. U augustu 2001 godine adaptiran je prostor za rad sa djecom od 2 do 5 godina starosti.<br><br>

Od 2011. godine Dom ne djeluje više na principu Dom-porodica. Prepoznavajući problematiku i potrebe korisnika formirane su homogene po spolu odgojne grupe. Te su postojale i postoje odgojne ženske grupe heterogene po uzrastu i mlađe muške odgojne grupe i starije muške odgojne grupe.<br><br>

U periodu od 2011. godine, a intenzivno od 2013. godine JU Dom za djecu bez roditeljskog staranja u Tuzli je ušao u proces transformacije, odnosno deinstitucionalizacije sa ciljem otvaranja i kreiranja novi usluga u sistemu socijalne zaštite a u skladu sa donesenim Pravilnikom. U sam proces transformacije do sada su uključeni resorno ministarstvo, NVO Hope and Homes for Children iz Sarajeva i Centri za socijalni rad sa područja Tuzlanskog kantona.<br>




