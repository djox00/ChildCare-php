<p> 
JU Dom za djecu bez roditeljskog staranja Tuzla djeluje na jednoj lokaciji kroz nekoliko organizacionih jedinica:<br><br>

Bebi odjel /odjel za bebe i djecu uzrasta do 6 godina<br><br>

Namijenjen optimalnom rastu i razvoju djece u najranijoj dobi koji podrazumijeva brigu, njegu i odgoj svakog djeteta a sa ciljem pružanja potpune i adekvatne brige u ranom rastu i razvoju, načinima ishrane i njezi djeteta. Podijeljen je u tri podorganizacione jedinice. Podrazumijeva zaseban anex zgrade namijenjen potrebama djece od rođenja do polaska u školu.<br><br>

Grupa beba – od 0 – 12 mjeseci starosti
Jaslički grupa – od 12 – 36 mjeseci starosti
Predškolska grupa – od 3 – 6 godina starosti
Školski odjel za smještaj djece školskog uzrasta (osnovna i srednja škola) organizovan po odgojni grupama razvrstanih po spolu i uzrastu. Odjel građen po principu stambenih zgrada, na četiri etaže po dva fizički odvojena stana.<br><br>

Tri muške odgojne grupe
Jedna ženska odgojna grupa
Prihvatilište za djecu

Prihvatilište za djecu namijenjeno je za djecu i mlade uzrasta od 6-18 godina. Cilj prihvatilišta je obezbijediti hitan i privremeni smještaj za djecu i mlade u trajanju do tri mjeseca za koje se utvrdi da su bez adekvatne roditeljske zaštite ili za djecu i mlade koje su žrtve radne, seksualne i ekonomske eksplatacije ili zatečena u skitnji i prosjaćenju.<br><br>

Materinski dom<br><br>

Materinski dom za smještaj majki pod rizikom od razdvajanja sa djetetom. Korisnik usluge može biti majka sa bebom i sa više djece od kojih jedno od njih mora biti mlađe od 3 godine starosti i trudnice.<br><br>

Svrha usluge je omogućiti privremeni smještaj trudnica i majki sa djecom sa ciljem sprečavanja razdvajanja djece od majki i pružanje različitih oblika podrške koji bi trebali rezultirati osposobljavanjem majke za dalju brigu o djetetu, mogućnosti povratka u biološku porodicu, podrška u pronalaženju zaposlenja i očuvanja i širenja socijalne mreže. Majka može boraviti u Materinskom domu najduže do godinu dana starosti djeteta.<br><br>

Dnevni centar za djecu

Dnevni centar je usluga za djecu u porodici pod rizikom od razdvajanja koja pruža različite aktivnosti kroz koje će djeca dobiti mogućnost da se razvijaju kroz socijalne, kreativne, radno-okupacione, edukativne, psihosocijalne aktivnosti uz podršku profesionalaca. Dijete može postati korisnik Dnevnog centra po preporuci nadležnog centra za socijalni rad. Dnevni centar je JU Dom za djecu bez roditeljskog staranja Tuzla pozitivno, sigurno, konstruktivno i kontrolirano okruženje koje podstiče optimalan razvoj individualnih potencijala djece.<br><br>

Mobilni tim<br><br>

Mobilni tim kao zasebna usluga egzistira od početka 2016. Kao podrška djeci koja su izašla iz sistema javne brige, smještena u hraniteljske porodice, povratak djece u biološke porodice. Mobilni tim kao usluga sa svojim aktivnostima će biti i usko je vezana za rad Dnevnog centra i Centra za edukaciju i savjetovanje.<br><br>

Centar za edukaciju i savjetovanje<br><br>

Centar za edukaciju i savjetovanje je usluga koja se nadovezuje na rad dnevnog centra. Centar roditeljima, starateljima, usvojiteljima i hraniteljima djece, roditeljima djece pod rizikom od razdvajanja nudi profesionalne usluge savjetovanja i edukacije kroz radionice, savjetodavni, pedagoški i psihosocijalni rad, a u cilju očuvanja porodičnog integriteta i funkcionalnosti porodice.<br><br>





